package ejercicio5;

public class SolucionEjercicio5 {
	int maxGanancia;
	int instrucciones;
	
	public SolucionEjercicio5()
	{
		super();
		this.maxGanancia = 0;
		this.instrucciones = 0;
	}
	
	public int getMaxGanancia() 
	{
		return maxGanancia;
	}
	
	public void setMaxGanancia(int maxGanancia) 
	{
		this.maxGanancia = maxGanancia;
	}
	
	public int getInstrucciones() 
	{
		return instrucciones;
	}
	
	public void setInstrucciones(int instrucciones) 
	{
		this.instrucciones = instrucciones;
	}
}
