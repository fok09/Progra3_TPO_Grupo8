package ejercicio2;

public class Tarea {
int inicio;
int fin;



public Tarea(int inicio, int fin) {
	super();
	this.inicio = inicio;
	this.fin = fin;
}

public int getInicio() {
	return inicio;
}
public void setInicio(int inicio) {
	this.inicio = inicio;
}
public int getFin() {
	return fin;
}
public void setFin(int fin) {
	this.fin = fin;
}


}
