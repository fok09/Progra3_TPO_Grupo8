package ejercicio3;

public class SolucionEjercicio3 {
int aristaSum;
int instrucciones;

public SolucionEjercicio3()
{
	this.aristaSum = this.instrucciones = 0;
}

public SolucionEjercicio3(int aristaSum, int instrucciones) {
	super();
	this.aristaSum = aristaSum;
	this.instrucciones = instrucciones;
}

public int getAristaSum() {
	return aristaSum;
}

public void setAristaSum(int aristaSum) {
	this.aristaSum = aristaSum;
}

public int getInstrucciones() {
	return instrucciones;
}

public void setInstrucciones(int instrucciones) {
	this.instrucciones = instrucciones;
}




}
